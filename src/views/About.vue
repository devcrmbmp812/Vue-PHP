    <h2>La enfermedad</h2>

    <p>La celiaquía o enfermedad celíaca (EC) es un proceso crónico, multiorgánico autoinmune,a​ que lesiona primeramente el intestino y puede dañar cualquier órgano o tejido corporal. Afecta a personas que presentan una predisposición genética. Está producida por una "intolerancia" permanente al gluten (conjunto de proteínas presentes en el trigo, avena, cebada y centeno –TACC– y productos derivados de estos cereales), pero no se trata de una simple intolerancia alimentaria ni mucho menos de una alergia, ni de un trastorno únicamente digestivo como tradicionalmente se consideraba. Actualmente se sabe que es realmente una enfermedad sistémica, ya que la respuesta inmunitaria anormal causada por el gluten puede dar lugar a la producción de diferentes autoanticuerpos que pueden atacar a cualquier parte del organismo.​ Sin un tratamiento estricto, puede provocar complicaciones de salud muy graves, entre las que cabe señalar diversos tipos de cáncer (tanto del aparato digestivo, con un incremento del riesgo del 60%, como de otros órganos), enfermedades cardiovasculares, trastornos neurológicos y psiquiátricos (conocidos como "neurogluten"), otras enfermedades autoinmunes y osteoporosis, así como enfermedad celíaca refractaria (que no responde al tratamiento con la dieta) y en casos raros (principalmente en niños) la denominada "crisis celíaca", de aparición súbita y que puede ser mortal</p>

